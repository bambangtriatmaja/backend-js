const nilai = 62;

if (nilai > 90) {
  console.log("Nilai A");
} else if (nilai > 80) {
  console.log("Nilai B");
} else {
  console.log("Nilai C");
}

/**
 * Short conditional menggunakan ternary operator (? dan :)
 * ? digunakan untuk kondisi if.
 * : digunakan untuk kondisi else.
 */
const age = 20;
age > 21 ? console.log("Sudah dewasa") : console.log("Belum dewasa");
